     private void sendEmails() {
    try {
        String sql = "SELECT c.c_email FROM customer c JOIN service_complete sc ON c.cid = sc.cid WHERE sc.result = 'Complate'";
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();

        while (rs.next()) {
            String to = rs.getString("c_email");
            String subject = "Service Completion Notification";
            String message = "Dear Customer,\n\nYour service is complete. Thank you for choosing our service.";

            sendEmail(to, subject, message);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, e);
    }
}

